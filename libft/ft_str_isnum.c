#include "libft.h"

int	ft_str_isnum(char *s)
{
	int	i;

	i = -1;
	if (s[0] == '-')
		++i;
	while (s[++i])
	{
		if (!(s[i] >= '0' && s[i] <= '9'))
			return (0);
	}
	return (1);
}
